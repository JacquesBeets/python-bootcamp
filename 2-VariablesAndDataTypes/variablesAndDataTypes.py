# VARIABLES ARE DYNAMICALLY TYPED - MEANING A VARIABLE IS ABLE TO CHANGE TYPE eg. FROM BOOLEAN TO A STRING

num_of_cats = 99 #stores a variable
print(num_of_cats * 2)

num_of_cats = num_of_cats - 40

print(num_of_cats)

friends = 0

friends = num_of_cats

print("Friends:",friends)

#Boolean - True or False must be in capital letters
game_over = False
print(type(game_over)) #type() tells us what type of variable this is

